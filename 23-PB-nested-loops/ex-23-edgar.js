`use strict`;
for (let i = 11; i >= 0; i--) { // outer loop
    let myString = '';
    for (let k = 10; k >= i; k--) { // nested loop // inner loop
        myString += ' ';
    }
    for (let j = 0; j <= i * 2; j++) { // nested loop // inner loop
        myString += '*';
    }
    for (let l = 10; l > i; l--) { // nested loop // inner loop
        myString += ' ';
    }
    console.log(myString);
}
